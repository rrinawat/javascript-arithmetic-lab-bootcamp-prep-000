//addition
function add(a,b){
  return a+b
}
//subtraction
function subtract(a,b){
  return a-b
}
//multiply
function multiply(a,b){
  return a * b
}
//divide
function divide(a,b){
  return a / b
}
//increment
function inc(n){
  n++
  return n
}
//decrement
function dec(n){
  n--
  return n
}
//make into integer
function makeInt(n){
  parseInt(n, 10)
  return n
}
//floating-point
function preserveDecimal(n){
  parseFloat(n)
  return n
}
